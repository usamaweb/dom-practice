const title = document.querySelector('#title')
const firstPara = document.querySelector('#first-paragraph')
const secondPara = document.querySelector('#second-paragraph')

function colorBlue() {
    title.style.color = 'blue'
    firstPara.style.color = 'blue'
    secondPara.style.color = 'blue'
}

function colorRed() {
    title.style.color = 'red'
    firstPara.style.color = 'red'
    secondPara.style.color = 'red'
}